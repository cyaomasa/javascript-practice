// varは再代入が可能
var nickname = "taro"
console.log(nickname)
nickname = "jiro"
console.log(nickname)

// letは再代入が可能
let nickname2 = "taro"
console.log(nickname2)
nickname2 = "jiro"
console.log(nickname2)

// constは再代入が不可
const nickname3 = "taro"
console.log(nickname3)
nickname3 = "jiro"
console.log(nickname3)
